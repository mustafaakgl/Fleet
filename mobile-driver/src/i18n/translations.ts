export const translations: Record<AppLocale, TranslationTree> = {
  en,
  de,
  tr,
  pl,
  nl,
  it,
  es,
  ru,
};

export function normalizeLocale(value?: string | null): AppLocale {
  if (value && value in translations) {
    return value as AppLocale;
  }
  return DEFAULT_LOCALE;
}

function resolveTranslationNode(locale: AppLocale, parts: string[]): string | null {
  const chain: AppLocale[] = [locale, 'de', 'en'];
  const seen = new Set<AppLocale>();

  for (const loc of chain) {
    if (seen.has(loc)) continue;
    seen.add(loc);

    let node: unknown = translations[loc];
    let found = true;
    for (const part of parts) {
      if (typeof node !== 'object' || node === null || !(part in node)) {
        found = false;
        break;
      }
      node = (node as Record<string, unknown>)[part];
    }
    if (found && typeof node === 'string') {
      return node;
    }
  }

  return null;
}

export function t(
  locale: AppLocale,
  key: string,
  params?: Record<string, string | number>,
): string {
  const parts = key.split('.');
  const node = resolveTranslationNode(locale, parts);
  if (!node) return key;
  if (!params) return node;
  return Object.entries(params).reduce(
    (acc, [k, v]) => acc.replaceAll(`{{${k}}}`, String(v)),
    node,
  );
}
